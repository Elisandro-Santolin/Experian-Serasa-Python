# 4. Construa um programa que leia 2 números reais que seja informados pelo usuário. Esse programa deve realizar soma e a multiplicação desses valores e exibir o resultado de ambos.

number1 = float(input('Digite o primeiro número: '))
number2 = float(input('Digite o segundo número: '))

sum = (number1+number2)
multiplication = (number1*number2)

print('A soma do dois números é:',sum)
print('A multiplicação do dois números é:',multiplication)